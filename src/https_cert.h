// ============================================================
//  https_cert.h  -  Embedded self-signed TLS certificate
//
//  A pre-generated self-signed certificate (RSA-2048) and its
//  private key, both in DER form, handed to ESPWebServerSecure
//  via setServerKeyAndCert().
//
//  Why embedded rather than generated on the device:
//  on-device key generation (createSelfSignedCert) corrupts the
//  heap and crashes on the esp32_idf5_https_server build for
//  arduino-esp32 3.3.7 / ESP-IDF 5.5.  Embedding a fixed cert
//  sidesteps that entirely and makes every boot instant.
//
//  Security note: this private key ships inside the firmware, so
//  it is NOT a per-device secret -- anyone with the firmware or
//  this file can read it.  That is acceptable for a self-signed
//  cert on a LAN device (a browser warns either way), but do not
//  treat HTTPS here as protection against a determined attacker
//  already on your network.
//
//  To regenerate (CN/validity to taste):
//    openssl req -x509 -newkey rsa:2048 -keyout k.pem -out c.pem \
//      -days 3650 -nodes -subj "/CN=m5stack-i2c.local"
//    openssl rsa  -in k.pem -outform DER -out k.der
//    openssl x509 -in c.pem -outform DER -out c.der
//  then xxd -i each .der file into the arrays below.
// ============================================================

